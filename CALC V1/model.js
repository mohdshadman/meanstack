function add(firstNo,secondNo){
    
    return parseInt(firstNo)+parseInt(secondNo);
}

function sub(firstNo,secondNo){
    return parseInt(firstNo)-parseInt(secondNo);
}

function mul(firstNo,secondNo){
    return parseInt(firstNo)*parseInt(secondNo);
}

function div(firstNo,secondNo){
    return parseInt(firstNo)/parseInt(secondNo);
}